"""
Repetições
while (enquanto)
Executa uma ação enquanto uma condição for verdadeira
Loop infinito -> Quando um código não tem fim
"""

contador = 0 # Inicialização do contador

while contador <= 10: # Condição de continuação do loop
    contador = contador + 1 # Atualização do contador
    print(contador) # Corpo do loop

print('Acabou') # Mensagem após o loop