"""
DocString
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234) # int

# Aspas simples
print('Luiz Otávio') # str
print(1, 'Luiz "Otávio"') # str

# Aspas duplas
print("Luiz Otávio") # str
print(2, "Luiz 'Otávio'") # str

# Escape
print("Luiz \"Otávio\"") # Usando barra invertida para escapar as aspas duplas

# r
print(r"Luiz \"Otávio\"") # Colocando o r antes das aspas, o Python ignora os escapes